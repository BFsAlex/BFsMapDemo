    self.mapView = mapView;
    mapView.showsUserLocation = YES;
}

/*
 显示GPS轨迹路线
 */
- (void)showGPSLineOnMapView {
    
    CLLocationCoordinate2D coordinates[self.points.count];
    for (int i = 0; i < self.points.count; i++) {
        BFsMapPointModel *pointModel = self.points[i];
        double longitude = [pointModel.coor_E doubleValue];
        double lantitude = [pointModel.coor_N doubleValue];
        coordinates[i] = CLLocationCoordinate2DMake(lantitude, longitude);
    }
    self.polyline = [MKPolyline polylineWithCoordinates:coordinates count:self.points.count];
    
    //
    if (self.polyline && ![self.mapView.overlays containsObject:self.polyline]) {
        [self.mapView addOverlay:self.polyline];
    }
    // 定位到一点
//    CLLocationCoordinate2D center = coordinates[0];
//    MKCoordinateSpan span;
//    span.longitudeDelta = 0.0001;
//    span.latitudeDelta = 0.0001;
//    MKCoordinateRegion regin = {center, span};
//    [self.mapView setRegion:regin animated:YES];
    
    [self startPlayGPSAnimation];
}

/*
 播放轨迹变化动画
 */
- (void)startPlayGPSAnimation {
    
    _gpsPointIndex = 0;
    self.gpsTimer = [NSTimer timerWithTimeInterval:0.5f target:self selector:@selector(updateNextGpsPoint) userInfo:nil repeats:YES];
    [[NSRunLoop currentRunLoop] addTimer:_gpsTimer forMode:NSRunLoopCommonModes];
}

- (void)updateNextGpsPoint {
    
    if (_gpsPointIndex >= self.points.count) {
        // 单次演示
//        if (_gpsTimer) {
//            [_gpsTimer invalidate];
//            _gpsTimer = nil;
//        }
//        return;
        
        // 无限循环
        _gpsPointIndex = 0;
    }
    
    BFsMapPointModel *pointModel = self.points[_gpsPointIndex++];
    double longitude = [pointModel.coor_E doubleValue];
    double lantitude = [pointModel.coor_N doubleValue];
    
    CLLocationCoordinate2D coordinate = CLLocationCoordinate2DMake(lantitude, longitude);
    //Update region
    MKCoordinateSpan span = MKCoordinateSpanMake(0.0001, 0.0001);
    MKCoordinateRegion region = MKCoordinateRegionMake(coordinate, span);
    [self.mapView setRegion:[self.mapView regionThatFits:region] animated:NO];
    
    [self.mapView removeAnnotations:self.mapView.annotations];
    //Add new annotation
    MKPointAnnotation *annotation = [[MKPointAnnotation alloc] init];
    annotation.coordinate = coordinate;
    [self.mapView addAnnotation:annotation];
}

#pragma mark - MKMapViewDelegate

- (MKAnnotationView *)mapView:(MKMapView *)mapView viewForAnnotation:(id<MKAnnotation>)annotation {
    static NSString* annoId = @"gpsAnnotation";
    MKAnnotationView* annoView = [mapView dequeueReusableAnnotationViewWithIdentifier:annoId];
    if (!annoView){
        annoView= [[MKAnnotationView alloc] initWithAnnotation:annotation reuseIdentifier:annoId];
    }
    annoView.image = [UIImage imageNamed:@"car.png"];
    return annoView;
}

-(MKOverlayRenderer*)mapView:(MKMapView*)mapView rendererForOverlay:(id <MKOverlay>)overlay {
    MKPolylineRenderer* lineView = [[MKPolylineRenderer alloc] initWithPolyline:self.polyline];
    lineView.strokeColor = [UIColor greenColor];
    lineView.lineWidth = 5.0;
    return lineView;
}


