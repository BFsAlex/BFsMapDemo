- (NSMutableArray *)points {
    
    if (!_points) {
        _points = [NSMutableArray array];
        NSArray *tmpPoints = [self prepareDemoPoints];
        for (NSString *point in tmpPoints) {
            BFsMapPointModel *pointModel = [[BFsMapPointModel alloc] init];
            //
            NSString *pointStr = [point copy];
            pointStr = [pointStr stringByReplacingOccurrencesOfString:@"N:" withString:@""];
            pointStr = [pointStr stringByReplacingOccurrencesOfString:@"E:" withString:@""];
            NSArray *pointArr = [pointStr componentsSeparatedByString:@" "];
            pointModel.index = pointArr[0];
            pointModel.date = pointArr[1];
            pointModel.time = pointArr[2];
            pointModel.coor_N = pointArr[3];
            pointModel.coor_E = pointArr[4];
            pointModel.speed = [NSString stringWithFormat:@"%@ %@", pointArr[5], pointArr[6]];
            pointModel.coor_X = pointArr[7];
            pointModel.coor_Y = pointArr[8];
            pointModel.coor_Z = pointArr[9];
            //
            [_points addObject:pointModel];
        }
    }
    
    return _points;
}

